package package03;

public class SquareExam {
    public static void main(String[] args) {
        // sqrt는 square root의 약자
        System.out.println(Math.sqrt(9));
        System.out.println(Math.sqrt(9.0f));
        System.out.println(Math.sqrt(9.0));
        System.out.println(Math.sqrt(-9.0));
    }
}
