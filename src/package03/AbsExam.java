package package03;

public class AbsExam {
    public static void main(String[] args) {
        // abs는 absolute(절대값)로, 무조건 양수 출력
        System.out.println(Math.abs(25));
        System.out.println(Math.abs(-25));
        System.out.println(Math.abs(25.5));
        System.out.println(Math.abs(-25.5));
    }
}
